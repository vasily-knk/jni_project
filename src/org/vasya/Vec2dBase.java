package org.vasya;

public class Vec2dBase {
    protected float x;
    protected float y;

    public float getX() {
        return this.x;
    }

    public void setX(float val) {
        this.x = val;
    }

    public float getY() {
        return this.y;
    }

    public void setY(float val) {
        this.y = val;
    }

}
